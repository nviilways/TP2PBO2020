    private void BPengembalianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BPengembalianActionPerformed
        // TODO add your handling code here:
        connect();
        String kode = TFKode.getText();
        int check = 0;
        
        String query = "SELECT * FROM buku";
        try{
            rs = stmt.executeQuery(query);
            while(rs.next()){
                if(kode.equals(rs.getString("kode_buku"))){
                    check = 1;
                }
            }
        } catch (Exception e){
            e.printStackTrace();
        }
        
        if(check == 1){
            try{
                String sql = "DELETE FROM buku WHERE kode_buku = '%s'";
                sql = String.format(sql, kode);
                stmt.execute(sql);
                
                stmt.close();
                conn.close();
                
            } catch (Exception e){
                e.printStackTrace();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Kode buku tidak ada !");
        }
        
        showPipi();
        clear();
        
    }//GEN-LAST:event_BPengembalianActionPerformed

